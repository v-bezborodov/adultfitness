    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'id_videos' => 'required|numeric',
            'comments' => 'required|max:1255',
            'id_parent' => 'numeric',
        ]);

        $all = $request->all();

        $all['id_users'] = $request->user->id;

        unset($all['api_token']);

        Comments::create($all);

        Videos::find($all['id_videos'])->increment('comments');

        return 200;
    }

